void Boom()
{
    ll n;
    cin >> n;
    vector<ll> v(n);
    for (ll i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    sort(v.begin() + 1, v.end());
    ll ans = v[0];
    for (ll i = 1; i < n; i++)
    {
        if (ans < v[i])
        {
            ans += (v[i] - ans + 1) / 2;
        }
    }
    cout << ans << endl;
}

