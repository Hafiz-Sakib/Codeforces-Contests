void Boom()
{
    ll n, cnt = 1, s = 1;
    cin >> n;
    vector<ll> v(n);
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    sort(v.begin(), v.end());
    for (ll i = 1; i < n; i++)
    {
        if (v[i] == v[i - 1] + 1)
        {
            cnt++;
        }
        else
        {
            s = max(s, cnt);
            cnt = 1;
        }
    }
    s = max(s, cnt);
    cout << n / s << endl;
}

